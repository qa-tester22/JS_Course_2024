/*
В JavaScript, `RegExp` (регулярное выражение) - это объект, который используется для работы с текстом, а не только со строками. Он позволяет осуществлять поиск, замену и сопоставление текста на основе шаблона, который представлен в виде строки или другого регулярного выражения.

Регулярные выражения могут использоваться не только для поиска и сопоставления строк, но также и для других типов данных, таких как массивы и даже числа, хотя их основное применение - это работа со строками.

Например, регулярные выражения могут быть использованы для поиска или фильтрации элементов массива на основе определенного шаблона, или для проверки соответствия строки определенному формату.

Примеры использования регулярных выражений в JavaScript:

1. Поиск подстроки в строке:


const str = "Hello, World!";
const pattern = /World/;
console.log(pattern.test(str)); // Выведет: true


2. Поиск элементов массива по шаблону:


const array = ["apple", "banana", "cherry"];
const pattern = /^b/;
const filteredArray = array.filter(item => pattern.test(item));
console.log(filteredArray); // Выведет: ["banana"]


3. Проверка соответствия числа определенному формату:


const number = 12345;
const pattern = /^\d{5}$/;
console.log(pattern.test(number)); // Выведет: true


Таким образом, регулярные выражения в JavaScript могут быть применены к различным типам данных, хотя их основное применение - это работа со строками.
*/