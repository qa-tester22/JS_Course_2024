/*
В данном коде выполняется декларирование функции `checkAnswer`. Декларация функции происходит с использованием ключевого слова `function`, после которого идет имя функции (`checkAnswer`), список параметров в скобках (в данном случае `selectedAnswer`), и тело функции в фигурных скобках.

const correctAnswer = "myFunction() {}";

function checkAnswer(selectedAnswer) {
    if (selectedAnswer === correctAnswer) {
        console.log("Правильно!");
    } else {
        console.log("Неправильно. Правильный ответ: " + correctAnswer);
    }
}


Таким образом, в результате декларации функции `checkAnswer`, эта функция становится доступной для вызова в любом месте кода после ее объявления. 

Когда вы вызываете функцию `checkAnswer` с каким-либо аргументом, она сравнивает этот аргумент с переменной `correctAnswer` и выводит сообщение о правильности или неправильности ответа в зависимости от совпадения значений. 

Декларация функции позволяет создавать и использовать функции в коде в простой и читаемой форме.
*/

/*
Декларация функции в JavaScript — это один из способов создания функций. При декларации функции используется ключевое слово `function`, за которым следует имя функции, список параметров в скобках (если функция принимает аргументы), и тело функции в фигурных скобках. Вот пример:


function greet(name) {
    console.log("Hello, " + name + "!");
}


В этом примере:
- `function` - это ключевое слово, которое указывает, что мы объявляем функцию.
- `greet` - это имя функции. Мы можем использовать это имя для вызова функции позже в коде.
- `(name)` - это список параметров функции. В этом случае функция принимает один аргумент, `name`.
- `{ ... }` - это тело функции. Внутри фигурных скобок содержится код, который будет выполнен при вызове функции.

Одно из особенностей декларации функции в JavaScript заключается в том, что она поднимается (hoisted) в начало области видимости. Это означает, что функцию можно вызвать до её фактического объявления в коде, и JavaScript все равно будет знать о ней. Например:


greet("Alice");

function greet(name) {
    console.log("Hello, " + name + "!");
}


Этот код будет работать корректно, даже если вызов `greet("Alice")` находится перед объявлением функции `greet`.

Также функции, объявленные с использованием декларации функции, могут быть использованы в качестве значения переменной, присваиваться другим переменным или передаваться в качестве аргументов другим функциям.
*/